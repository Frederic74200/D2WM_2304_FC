namespace ex_1_3_moyenne_2_nombres
{
    internal class Program
    {
        static void Main(string[] args)
        {

         // VARIABLES
            int nombre1;
            int nombre2;
            float moyenne;

          // TRAITEMENT
            Console.WriteLine("Veuillez saisir le premier nombre.");
            nombre1 = Convert.ToInt32(Console.ReadLine());

            Console.WriteLine("Veuillez saisir le premier nombre.");
            nombre2 = Convert.ToInt32(Console.ReadLine());

            moyenne = (nombre1 + nombre2) / 2f;

        // FIN TRAITEMENT


          // AFFICHAGE
            Console.WriteLine("La moyenne des nombres : "+nombre1+" et "+nombre2
                +" est : "+moyenne);

            Console.WriteLine("Appuyez sur une touche pour quitter.");
            Console.ReadLine();
        // FIN PROGRAMME
                
        }
    }
